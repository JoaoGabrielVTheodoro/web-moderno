/**
 * Operadores: Atribuição
 * - Atribui um valor ao operando a sua esquerda
 * - O operador de atribuição básico é o igual (=), que atribui o valor do operando à direita ao operando à esquerda. Isto é, x = y atribui o valor de y a x
*/

const a = 7
let b = 3

b += a // atribuição aditiva - acrescentando a a b b = b + a
console.log(b)

b -= 4 // atribuição subtrativa - b = b - 4
console.log(b)

b *= 2 // atribuição multiplicativa - b = b * 2
console.log(b)

b /= 2 // atribuição de divisão - b = b / 2
console.log(b)

b %= 2 // Atribuição de resto (modular) - b = b % 2
console.log(b)
