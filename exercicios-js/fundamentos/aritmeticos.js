/**
 * Operadores: Aritméticos
 * - Tomam dois valores numéricos (literais, variáveis), como seus operadores
 * - Retornam um único valor numérico
 * - Operadores padrões:
 *    - + → soma
 *    - - → subtração
 *    - * → multiplicação
 *    - / → divisão
 *    - % → módulo
 * - Trabalha como na maioria das Linguagens de Programação
*/

const [a, b, c, d] = [3, 5, 1, 15]

const soma = a + b + c + d
const subtracao = d - b
const multiplicacao = a * b
const divisao = d / a
const modulo = a % 2

console.log(soma, subtracao, multiplicacao, divisao, modulo)
