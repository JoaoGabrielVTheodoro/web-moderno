/**
 * IIFE
 * - Imediately Invoked Function
 * - Designe Pattern  → Self-Executing Anonymous Function
 * - Variáveis dentro do escopo léxico
 * - Evita o Escopo Global
 * - Declarada e invocada
 * - (function(){})()
*/

(function () {
  console.log('Será executado na hora!')
  console.log('Fohe do escopo mais abrangente!')
})()
